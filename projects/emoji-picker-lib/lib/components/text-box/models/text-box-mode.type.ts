/**
 * Specifies the mode of a text box component, determining its functionality and behavior.
 *
 * - `text`: A standard text input field.
 * - `search`: A text input field intended for search queries, often paired with search-related features.
 * - `password`: A text input field that masks the entered text.
 *
 * @group Types
 */
export type TextBoxMode = 'text' | 'search' | 'password';
