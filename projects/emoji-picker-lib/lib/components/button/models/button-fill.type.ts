/**
 * Specifies the fill style options for a button component.
 *
 * - `clear`: The button has no background fill, making it appear transparent or minimally styled.
 * - `outlined`: The button has a transparent background with a border, giving it an outlined appearance.
 * - `solid`: The button has a solid background color, making it fully filled.
 *
 * @group Types
 */
export type ButtonFill = 'clear' | 'outlined' | 'solid';
