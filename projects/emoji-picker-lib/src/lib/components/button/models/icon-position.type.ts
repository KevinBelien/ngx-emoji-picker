/**
 * Specifies the possible positions for an icon within a button component relative to the button's text.
 *
 * - `left`: The icon is positioned to the left of the button's text.
 * - `right`: The icon is positioned to the right of the button's text.
 * - `top`: The icon is positioned above the button's text.
 * - `bottom`: The icon is positioned below the button's text.
 *
 * @group Types
 */
export type IconPosition = 'left' | 'right' | 'top' | 'bottom';
